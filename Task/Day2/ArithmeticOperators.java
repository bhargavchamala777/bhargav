package Day2;

public class ArithmeticOperators {

		public static void main(String[] args) {
			int x = 15;
			int y = 10;
			
			System.out.println(x + y); 
			System.out.println(x - y); 
			System.out.println(x * y);		
			System.out.println(x / y); 
			System.out.println(15.0f / 10); 
			System.out.println(15.0 / 10); 
			
			System.out.println(15 % 10); 
			System.out.println(-15 % 10); 
			System.out.println(15 % -10); 
			System.out.println(-15 % -10); 	
			
				}
	}



